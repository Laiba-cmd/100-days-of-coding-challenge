// Equality with strings
console.log("Testing equality with strings:");
console.log("kiwi" == "kiwi"); // True
console.log("kiwi" == "kiwi"); // False
// Using the lower case function
console.log("Testing with lower case:");
console.log("Kiwi".toLowerCase() == "kiwi"); // True
// Numerical tests
console.log("Numerical tests:");
console.log(9 > 7); // True
console.log(5 < 2); // False
// Tests using "and" and "or" operators
console.log("Tests with 'and' and 'or':");
console.log(true && false); // False
console.log(true || false); // True
// Test whether an item is in a array
var fruits = ["kiwi", "orange", "cherry"];
console.log("Is 'kiwi' in fruits?");
console.log(fruits.includes("kiwi")); // True
// Test whether an item is not in a array
console.log("Is 'mango' not in fruits?");
console.log(!fruits.includes("mango")); // True
